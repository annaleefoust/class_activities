package class_implementation;

public class Util {
	int result = 0;
	public void sum(int a, int b){
	result = a + b;
	System.out.println("Sum Result = " + result);
	}
	public void sum(int a, int b, int c){
	result = a + b + c;
	System.out.println("Sum Result = " + result);
	}
	public void sum(int a, int b, int c, int d){
	result = a + b + c + d;
	System.out.println("Sum Result = " + result);
	}
	
}
